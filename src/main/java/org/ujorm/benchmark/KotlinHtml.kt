package org.ujorm.benchmark

import kotlinx.html.*
import kotlinx.html.stream.appendHTML

/** Helper object for Kotlinx HTML generation */
object KotlinHtml {

    /** Renders the fortunes list into the provided appendable */
    fun renderFortunes(fortunes: List<HtmlBenchmark.Fortune>, appendable: Appendable) {
        appendable.appendHTML(prettyPrint = false).html {
            body {
                table {
                    for (fortune in fortunes) {
                        tr {
                            td { +fortune.id().toString() }
                            td { +fortune.message() }
                            td { +fortune.author() }
                        }
                    }
                }
            }
        }
    }

    /** Renders a more realistic page layout with conditions and nested loops. */
    fun renderComplexPage(
        fortunes: List<HtmlBenchmark.Fortune>,
        menuItems: List<String>,
        showPromo: Boolean,
        appendable: Appendable
    ) {
        appendable.appendHTML(prettyPrint = false).html {
            body {
                header {
                    h1 { +"Fortune Board" }
                }
                nav {
                    ul {
                        for (item in menuItems) {
                            li { +item }
                        }
                    }
                }
                if (showPromo) {
                    p { +"Promo mode is enabled." }
                }
                section {
                    table {
                        for (fortune in fortunes) {
                            tr {
                                td { +fortune.id().toString() }
                                td { +fortune.message() }
                                td { +fortune.author() }
                                td {
                                    ul {
                                        for (token in HtmlBenchmark.tokensOf(fortune.message())) {
                                            li { +token }
                                        }
                                    }
                                }
                            }
                        }
                    }
                }
                footer {
                    p { +"Generated by Kotlinx.html benchmark." }
                }
            }
        }
    }
}