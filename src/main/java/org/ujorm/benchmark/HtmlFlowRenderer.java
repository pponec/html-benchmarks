package org.ujorm.benchmark;

import htmlflow.HtmlFlow;

final class HtmlFlowRenderer {

    private HtmlFlowRenderer() {}

    static String render(HtmlBenchmark.BenchmarkModel model, HtmlBenchmark.Scenario scenario) {
        if (!scenario.isComplex()) {
            return HtmlFlow.view(view -> view
                    .html().body().table()
                    .of(table -> {
                        for (var fortune : model.fortunes()) {
                            table.tr()
                                    .td().text(String.valueOf(fortune.id())).__()
                                    .td().text(fortune.message()).__()
                                    .td().text(fortune.author()).__()
                                    .__();
                        }
                    }).__().__().__()
            ).render();
        }
        return HtmlFlow.view(view -> view
                .html().body()
                .header().h1().text("Fortune Board").__().__()
                .nav().ul().of(ul -> {
                    for (var item : model.menuItems()) {
                        ul.li().text(item).__();
                    }
                }).__().__()
                .of(body -> {
                    if (model.showPromo()) {
                        body.p().text("Promo mode is enabled.").__();
                    }
                })
                .section().table().of(table -> {
                    for (var fortune : model.fortunes()) {
                        table.tr()
                                .td().text(String.valueOf(fortune.id())).__()
                                .td().text(fortune.message()).__()
                                .td().text(fortune.author()).__()
                                .td().ul().of(ul -> {
                                    for (var token : HtmlBenchmark.tokensOf(fortune.message())) {
                                        ul.li().text(token).__();
                                    }
                                }).__().__()
                                .__();
                    }
                }).__().__()
                .footer().p().text("Generated by HtmlFlow benchmark.").__().__()
                .__().__()
        ).render();
    }
}
