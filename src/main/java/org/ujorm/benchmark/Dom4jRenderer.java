package org.ujorm.benchmark;

import java.io.IOException;
import java.io.StringWriter;
import java.io.Writer;

final class Dom4jRenderer {

    private Dom4jRenderer() {}

    static void render(HtmlBenchmark.BenchmarkModel model, HtmlBenchmark.Scenario scenario, Writer writer) throws IOException {
        var document = org.dom4j.DocumentHelper.createDocument();
        var html = document.addElement("html");
        var body = html.addElement("body");
        if (scenario.isComplex()) {
            body.addElement("header").addElement("h1").addText("Fortune Board");
            var nav = body.addElement("nav").addElement("ul");
            for (var item : model.menuItems()) {
                nav.addElement("li").addText(item);
            }
            if (model.showPromo()) {
                body.addElement("p").addText("Promo mode is enabled.");
            }
            body = body.addElement("section");
        }
        var table = body.addElement("table");

        for (var fortune : model.fortunes()) {
            var tr = table.addElement("tr");
            tr.addElement("td").addText(String.valueOf(fortune.id()));
            tr.addElement("td").addText(fortune.message());
            tr.addElement("td").addText(fortune.author());
            if (scenario.isComplex()) {
                var ul = tr.addElement("td").addElement("ul");
                for (var token : HtmlBenchmark.tokensOf(fortune.message())) {
                    ul.addElement("li").addText(token);
                }
            }
        }
        if (scenario.isComplex()) {
            document.getRootElement()
                    .element("body")
                    .addElement("footer")
                    .addElement("p")
                    .addText("Generated by Dom4j benchmark.");
        }

        var outputFormat = org.dom4j.io.OutputFormat.createCompactFormat();
        outputFormat.setSuppressDeclaration(true);
        var xmlWriter = new org.dom4j.io.XMLWriter(writer, outputFormat);
        xmlWriter.write(document);
    }

    static String renderToString(HtmlBenchmark.BenchmarkModel model, HtmlBenchmark.Scenario scenario) throws IOException {
        try (var writer = new StringWriter()) {
            render(model, scenario, writer);
            return writer.toString();
        }
    }
}
