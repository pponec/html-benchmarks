package org.ujorm.benchmark;

import java.io.IOException;
import java.io.StringWriter;
import java.io.Writer;

import static j2html.TagCreator.*;

final class J2htmlRenderer {

    private J2htmlRenderer() {}

    static void render(HtmlBenchmark.BenchmarkModel model, HtmlBenchmark.Scenario scenario, Writer writer) throws IOException {
        if (scenario.isComplex()) {
            html(
                    body(
                            header(h1("Fortune Board")),
                            nav(ul(each(model.menuItems(), item -> li(item)))),
                            model.showPromo() ? p("Promo mode is enabled.") : null,
                            section(
                                    table(
                                            each(model.fortunes(), fortune -> tr(
                                                    td(String.valueOf(fortune.id())),
                                                    td(fortune.message()),
                                                    td(fortune.author()),
                                                    td(ul(each(HtmlBenchmark.tokensOf(fortune.message()), token -> li(token))))
                                            ))
                                    )
                            ),
                            footer(p("Generated by j2html benchmark."))
                    )
            ).render(writer);
        } else {
            html(
                    body(
                            table(
                                    each(model.fortunes(), fortune -> tr(
                                            td(String.valueOf(fortune.id())),
                                            td(fortune.message()),
                                            td(fortune.author())
                                    ))
                            )
                    )
            ).render(writer);
        }
    }

    static String renderToString(HtmlBenchmark.BenchmarkModel model, HtmlBenchmark.Scenario scenario) throws IOException {
        try (var writer = new StringWriter()) {
            render(model, scenario, writer);
            return writer.toString();
        }
    }
}
