package org.ujorm.benchmark;

final class JsoupRenderer {

    private JsoupRenderer() {}

    static String render(HtmlBenchmark.BenchmarkModel model, HtmlBenchmark.Scenario scenario) {
        var doc = new org.jsoup.nodes.Document("");
        doc.outputSettings().prettyPrint(false);
        var body = doc.appendElement("html").appendElement("body");
        if (scenario.isComplex()) {
            body.appendElement("header").appendElement("h1").text("Fortune Board");
            var nav = body.appendElement("nav").appendElement("ul");
            for (var item : model.menuItems()) {
                nav.appendElement("li").text(item);
            }
            if (model.showPromo()) {
                body.appendElement("p").text("Promo mode is enabled.");
            }
            body = body.appendElement("section");
        }
        var table = body.appendElement("table");

        for (var fortune : model.fortunes()) {
            var tr = table.appendElement("tr");
            tr.appendElement("td").text(String.valueOf(fortune.id()));
            tr.appendElement("td").text(fortune.message());
            tr.appendElement("td").text(fortune.author());
            if (scenario.isComplex()) {
                var ul = tr.appendElement("td").appendElement("ul");
                for (var token : HtmlBenchmark.tokensOf(fortune.message())) {
                    ul.appendElement("li").text(token);
                }
            }
        }
        if (scenario.isComplex()) {
            doc.body().appendElement("footer").appendElement("p").text("Generated by Jsoup benchmark.");
        }

        return doc.outerHtml();
    }
}
