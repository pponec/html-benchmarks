package org.ujorm.benchmark;

import org.ujorm.tools.web.AbstractHtmlElement;
import org.ujorm.tools.xml.config.HtmlConfig;

import java.io.IOException;
import java.io.StringWriter;
import java.io.Writer;

final class UjormRenderer {

    private UjormRenderer() {}

    static void render(HtmlBenchmark.BenchmarkModel model, HtmlBenchmark.Scenario scenario, HtmlConfig config, Writer writer) throws IOException {
        try (var html = AbstractHtmlElement.of(writer, config)) {
            try (var body = html.addBody()) {
                if (scenario.isComplex()) {
                    try (var header = body.addElement("header")) {
                        header.addHeading("Fortune Board");
                    }
                    try (var nav = body.addElement("nav").addUnorderedlist()) {
                        for (var item : model.menuItems()) {
                            nav.addListItem().addText(item);
                        }
                    }
                    if (model.showPromo()) {
                        body.addParagraph().addText("Promo mode is enabled.");
                    }
                    try (var section = body.addElement("section");
                         var table = section.addTable()) {
                        for (var fortune : model.fortunes()) {
                            try (var row = table.addTableRow()) {
                                row.addTableDetail().addText(fortune.id());
                                row.addTableDetail().addText(fortune.message());
                                row.addTableDetail().addText(fortune.author());
                                try (var td = row.addTableDetail().addUnorderedlist()) {
                                    for (var token : HtmlBenchmark.tokensOf(fortune.message())) {
                                        td.addListItem().addText(token);
                                    }
                                }
                            }
                        }
                    }
                    try (var footer = body.addElement("footer")) {
                        footer.addParagraph().addText("Generated by Ujorm benchmark.");
                    }
                } else {
                    try (var table = body.addTable()) {
                        for (var fortune : model.fortunes()) {
                            try (var row = table.addTableRow()) {
                                row.addTableDetail().addText(fortune.id());
                                row.addTableDetail().addText(fortune.message());
                                row.addTableDetail().addText(fortune.author());
                            }
                        }
                    }
                }
            }
        }
    }

    static String renderToString(HtmlBenchmark.BenchmarkModel model, HtmlBenchmark.Scenario scenario, HtmlConfig config) throws IOException {
        try (var writer = new StringWriter()) {
            render(model, scenario, config, writer);
            return writer.toString();
        }
    }
}
