package org.ujorm.benchmark;

final class SafeStringBuilderRenderer {

    private SafeStringBuilderRenderer() {}

    static String render(HtmlBenchmark.BenchmarkModel model, HtmlBenchmark.Scenario scenario) {
        var html = new SafeHtmlStringBuilder(4096);
        html.raw("<html><body>");
        if (scenario.isComplex()) {
            html.raw("<header><h1>Fortune Board</h1></header><nav><ul>");
            for (var item : model.menuItems()) {
                html.element("li", item);
            }
            html.raw("</ul></nav>");
            if (model.showPromo()) {
                html.element("p", "Promo mode is enabled.");
            }
            html.raw("<section>");
        }
        html.raw("<table>");
        for (var fortune : model.fortunes()) {
            html.openTag("tr")
                    .element("td", fortune.id())
                    .element("td", fortune.message())
                    .element("td", fortune.author());
            if (scenario.isComplex()) {
                html.openTag("td").openTag("ul");
                for (var token : HtmlBenchmark.tokensOf(fortune.message())) {
                    html.element("li", token);
                }
                html.closeTag("ul").closeTag("td");
            }
            html.closeTag("tr");
        }
        html.raw("</table>");
        if (scenario.isComplex()) {
            html.raw("</section><footer><p>Generated by StringBuilder benchmark.</p></footer>");
        }
        html.raw("</body></html>");
        return html.toString();
    }
}
